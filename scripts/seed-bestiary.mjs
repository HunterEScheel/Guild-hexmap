async function main() {
  console.log("-- Bestiary seed data from dnd5eapi.co with curated terrains");
  console.log("-- Generated on", new Date().toISOString());
  console.log("");

  const listRes = await fetch(`${API_BASE}/monsters`);
  const listData = await listRes.json();
  const monsters = listData.results;

  console.log("INSERT INTO bestiary (index, name, type, size, cr, xp, hp, ac, terrains) VALUES");

  const rows = [];

  for (let i = 0; i < monsters.length; i++) {
    const ref = monsters[i];
    const terrains = MONSTER_TERRAINS[ref.index];
    if (!terrains) continue; // skip monsters without curated terrains

    const res = await fetch(`${API_BASE}/monsters/${ref.index}`);
    if (!res.ok) continue;
    const m = await res.json();

    const ac = m.armor_class?.[0]?.value ?? 10;
    const esc = (s) => s.replace(/'/g, "''");
    const terrainsArr = `'{${terrains.map(t => `"${t}"`).join(",")}}'`;

    rows.push(
      `  ('${esc(m.index)}', '${esc(m.name)}', '${esc(m.type || "")}', '${esc(m.size || "")}', ${m.challenge_rating ?? 0}, ${m.xp ?? 0}, ${m.hit_points ?? 0}, ${ac}, ${terrainsArr})`
    );

    // Rate limit: small delay every 10 requests
    if (i % 10 === 9) await new Promise((r) => setTimeout(r, 200));
  }

  console.log(rows.join(",\n"));
  console.log("ON CONFLICT (index) DO UPDATE SET");
  console.log("  name = EXCLUDED.name,");
  console.log("  type = EXCLUDED.type,");
  console.log("  size = EXCLUDED.size,");
  console.log("  cr = EXCLUDED.cr,");
  console.log("  xp = EXCLUDED.xp,");
  console.log("  hp = EXCLUDED.hp,");
  console.log("  ac = EXCLUDED.ac,");
  console.log("  terrains = EXCLUDED.terrains;");
}

main().catch(console.error);
